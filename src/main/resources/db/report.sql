CREATE TABLE REPORT  (
	ID BIGINT  NOT NULL PRIMARY KEY ,
	SALES BIGINT,
	QTY BIGINT ,
	STAFF_NAME VARCHAR(100) NOT NULL,
	DATE TIMESTAMP 
) ;


CREATE TABLE SOURCE_REPORT  (
	ID BIGINT  NOT NULL PRIMARY KEY ,
	SALES BIGINT,
	QTY BIGINT ,
	STAFF_NAME VARCHAR(100) NOT NULL,
	DATE TIMESTAMP 
) ;


